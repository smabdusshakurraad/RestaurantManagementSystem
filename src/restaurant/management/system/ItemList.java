    private void txbDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txbDeleteActionPerformed
        // TODO add your handling code here:
        int row = itemList.getSelectedRow();
        String cell = itemList.getModel().getValueAt(row,0).toString();
        try
        {
            Connection conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","HR","hr");
                String sql1 = "Delete from items where items_id ="+cell ;
                
                PreparedStatement ps = conn.prepareStatement(sql1);
                
                ps.executeQuery();
                JOptionPane.showMessageDialog(null,"Item deleted successfully");
                DefaultTableModel tblmodel = (DefaultTableModel)itemList.getModel();
                tblmodel.setRowCount(0);
                listTable();
                
        }
        
        catch(Exception ex)
            {
                JOptionPane.showMessageDialog(this, ex);
            }
    }//GEN-LAST:event_txbDeleteActionPerformed

