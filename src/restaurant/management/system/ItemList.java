    public void listTable()
    {
                        try
            {
                Connection conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","HR","hr");
                String sql1 = "select * from items";
                
                PreparedStatement ps = conn.prepareStatement(sql1);
           
                ResultSet rs = ps.executeQuery();
                
                while(rs.next())
                {
                    String id = String.valueOf(rs.getInt("ITEMS_ID"));
                    String name = rs.getString("ITEMS_NAME");
                    String type = rs.getString("ITEMS_TYPE");
                    String qty = String.valueOf(rs.getInt("QUANTITY"));
                    String category = rs.getString("ITEMS_CATEGORY");
                    String price = String.valueOf(rs.getInt("price"));
                    
                    String tbData[] = {id,name,type,qty,category,price};
                    DefaultTableModel tblmodel = (DefaultTableModel)itemList.getModel();
                    
                    tblmodel.addRow(tbData);
                }
                
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(this, ex);
            }
    }

