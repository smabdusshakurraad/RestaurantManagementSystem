    private void txbUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txbUpdateActionPerformed
        // TODO add your handling code here:
        
        if(txbId.getText().trim().equals("") || txbName.getText().trim().equals("") || txbType.getText().trim().equals("") || txbQty.getText().trim().equals("") || txbCategory.getText().trim().equals("") || txbPrice.getText().trim().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Fillup all the forms");
            
        }
        
        else
        {
            try
            {
                int id = Integer.parseInt(txbId.getText().trim());
                Connection conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","HR","hr");
                String sql1 = "select * from items where items_id ="+id;
                
                PreparedStatement ps = conn.prepareStatement(sql1);
           
                ResultSet rs = ps.executeQuery();
                
                if(rs.next())
                {
                   String sql = "Update items SET items_name =?, items_type = ?,quantity = ?, items_category = ?, price =? where items_id ="+id;
                   PreparedStatement ps1 = conn.prepareStatement(sql);
                   ps1.setString(1, txbName.getText().trim());
                   ps1.setString(2, txbType.getText().trim());
                   ps1.setString(3, txbQty.getText().trim());
                   ps1.setString(4, txbCategory.getText().trim());
                   ps1.setString(5, txbPrice.getText().trim());
                   ps1.executeUpdate();
                   
                   DefaultTableModel tblmodel = (DefaultTableModel)itemList.getModel();
                   tblmodel.setRowCount(0);
                   listTable();
                   JOptionPane.showMessageDialog(null,"Update successful");
                }
                else
                {
                   String sql = "insert into items (items_id, items_name, items_type,quantity, items_category, price) values (?,?,?,?,?,?)";
                   PreparedStatement ps1 = conn.prepareStatement(sql);
                   ps1.setString(1, txbId.getText().trim());
                   ps1.setString(2, txbName.getText().trim());
                   ps1.setString(3, txbType.getText().trim());
                   ps1.setString(4, txbQty.getText().trim());
                   ps1.setString(5, txbCategory.getText().trim());
                   ps1.setString(6, txbPrice.getText().trim());
                   ps1.executeUpdate();
                   
                   DefaultTableModel tblmodel = (DefaultTableModel)itemList.getModel();
                   tblmodel.setRowCount(0);
                   listTable();
                   JOptionPane.showMessageDialog(null,"Update successful");
                }
                txbId.setText("");
                txbName.setText("");
                txbType.setText("");
                txbQty.setText("");
                txbCategory.setText("");
                txbPrice.setText("");
            }
            
            
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(this, ex);
            }
           
            
            
            
           
        }
    }//GEN-LAST:event_txbUpdateActionPerformed

